@Injectable({ providedIn: 'root' })
export class TranslationService {
  private readonly document = inject(DOCUMENT);
  private readonly storageKey = 'xj-language';
  private readonly languageSubject = new BehaviorSubject<Language>(
    this.resolveInitialLanguage()
  );

  readonly language$ = this.languageSubject.asObservable();

  constructor() {
    this.applyLanguage(this.languageSubject.value);
  }

  get currentLanguage(): Language {
    return this.languageSubject.value;
  }

  setLanguage(language: Language): void {
    if (language === this.currentLanguage) {
      return;
    }

    this.languageSubject.next(language);
    this.applyLanguage(language);

    try {
      localStorage.setItem(this.storageKey, language);
    } catch {
      // Ignore storage issues and keep the in-memory language.
    }
  }

  instant(key: string): string {
    return (
      translations[this.currentLanguage][key] ??
      translations.en[key] ??
      key
    );
  }

  private resolveInitialLanguage(): Language {
    try {
      const stored = localStorage.getItem(this.storageKey);
      if (stored === 'en' || stored === 'zh') {
        return stored;
      }
    } catch {
      // Ignore local storage issues and fall back to browser language.
    }

    return navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en';
  }

  private applyLanguage(language: Language): void {
    this.document.documentElement.lang = language === 'zh' ? 'zh-CN' : 'en';
  }
}
